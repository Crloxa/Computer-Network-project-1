void testQRCodeCellComparison() {
    // 生成二维码
    QRPosition qrPosition;
    vector<uint8_t> testData(Config::QRCode::BytesPerFrame, 0x55);
    Mat qrFrame;
    bool success = qrPosition.encodeFrameOriginalSize(0, 1, testData.data(), testData.size(), qrFrame);

    if (success) {
        // 保存二维码图片
        imwrite("test_qr.png", qrFrame);
        cout << "[INFO] QR code generated and saved as test_qr.png" << endl;

        // 从文件中读取二维码图片
        Mat readFrame = imread("test_qr.png");
        if (readFrame.empty()) {
            cout << "[ERROR] Failed to read test_qr.png" << endl;
            return;
        }
        cout << "[INFO] Read test_qr.png successfully" << endl;
        cout << "[DEBUG] Read frame size: " << readFrame.rows << "x" << readFrame.cols << endl;

        // 解码二维码
        Mat decodedFrame;
        bool extracted = extractQRCode(readFrame, decodedFrame);
        cout << "[DEBUG] Extracted frame size: " << decodedFrame.rows << "x" << decodedFrame.cols << endl;

        if (extracted) {
            // 获取编码时使用的数据单元格
            auto encodedCells = qrPosition.getMergedDataCells();
            cout << "[DEBUG] Encoded cells count: " << encodedCells.size() << endl;

            // 获取解码时使用的数据单元格
            auto decodedCells = qrPosition.getMergedDataCells();
            cout << "[DEBUG] Decoded cells count: " << decodedCells.size() << endl;

            // 比较前10个数据单元格的坐标
            cout << "[DEBUG] Comparing first 10 cells:" << endl;
            for (int i = 0; i < min(10, (int)encodedCells.size()); i++) {
                cout << "[DEBUG] Cell " << i << ": encoded= (" << encodedCells[i].row << ", " << encodedCells[i].col 
                     << "), decoded= (" << decodedCells[i].row << ", " << decodedCells[i].col << ")" << endl;
            }

            // 读取数据
            vector<uint8_t> frameData(Config::QRCode::BytesPerFrame, 0);
            int bitIndex = 0;

            for (const auto& cell : decodedCells) {
                Vec3b pixel = decodedFrame.at<Vec3b>(cell.row, cell.col);
                bool bit = (pixel[0] > 128 && pixel[1] > 128 && pixel[2] > 128); // White means 1

                int byteIndex = bitIndex / 8;
                int offset = bitIndex % 8;
                if (bit) frameData[byteIndex] |= (1 << offset);

                bitIndex++;
            }

            // 输出解码后的数据
            cout << "[INFO] Decoded data: " << endl;
            for (int i = 0; i < 16; i++) {
                cout << hex << setw(2) << setfill('0') << (int)frameData[i] << " ";
                if ((i + 1) % 8 == 0) cout << endl;
            }
            cout << dec << endl;

            // 验证数据是否一致
            bool dataMatch = true;
            for (int i = 0; i < Config::QRCode::BytesPerFrame; i++) {
                if (testData[i] != frameData[i]) {
                    dataMatch = false;
                    cout << "[ERROR] Data mismatch at index " << i << ": expected " << hex << (int)testData[i] << ", got " << (int)frameData[i] << dec << endl;
                    break;
                }
            }

            if (dataMatch) {
                cout << "[SUCCESS] Data matched!" << endl;
            }
            else {
                cout << "[ERROR] Data mismatch!" << endl;
            }
        }
    }
}

